#Ejemplos de operadores de Comparacion
#variables
m = int(input('Ingrese el valor de m: '))
n = int(input('Ingrese el valor de n: '))
print('')
print(f'{m} es igual que {n}? = {m == n}') #igualdad
print(f'{m} es mayor que {n}? = {m > n}') #mayor que
print(f'{m} es menor que {n}? = {m < n}') #menor que
print(f'{m} es mayor o igual que {n}? = {m >= n}') #mayor o igual que
print(f'{m} es menor o igual que {n}? = {m <= n}') #menor o igual que
print(f'{m} es diferente de {n}? = {m != n}') #diferente